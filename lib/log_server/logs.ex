defmodule LogServer.Logs do
  @moduledoc """
  The Logs context.
  """

  import Ecto.Query, warn: false
  alias LogServer.Repo
  alias LogServer.Logs.Event

  alias LogServer.Logs.Log

  @doc """
  Returns the list of logs.

  ## Examples

      iex> list_logs()
      [%Log{}, ...]

  """
  def list_logs do
    Repo.all(Log)
  end

  def logs_for_user(user_id) do
    query = from(log in Log, where: log.user_id == ^user_id)
    {:ok,  Repo.all(query) }
  end


  def events_for_log(log_id) do
    query = from(event in Event, where: event.log_id == ^log_id)
    {:ok,  Repo.all(query) }
  end


  @doc """
  Gets a single log.

  Raises `Ecto.NoResultsError` if the Log does not exist.

  ## Examples

      iex> get_log!(123)
      %Log{}

      iex> get_log!(456)
      ** (Ecto.NoResultsError)

  """
  def get_log!(id), do: Repo.get!(Log, id)

  @doc """
  Creates a log.

  ## Examples

      iex> create_log(%{field: value})
      {:ok, %Log{}}

      iex> create_log(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_log(attrs \\ %{}) do

    log_type = attrs[:log_type]
    little_map = %{log_type: string_value_of_log_type(attrs[:log_type])}
    attrs2 = Map.merge attrs, little_map

    %Log{}
    |> Log.changeset(attrs2)
    |> Repo.insert()

  end

  def string_value_of_log_type(tipe) do
    case tipe do
      :INTEGER -> "INTEGER"
      :FLOAT -> "FLOAT"
      :DATETIME -> "DATETime"
      _ -> "NONE"
    end
  end

  @doc """
  Updates a log.

  ## Examples

      iex> update_log(log, %{field: new_value})
      {:ok, %Log{}}

      iex> update_log(log, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_log(%Log{} = log, attrs) do
    log
    |> Log.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a Log.

  ## Examples

      iex> delete_log(log)
      {:ok, %Log{}}

      iex> delete_log(log)
      {:error, %Ecto.Changeset{}}

  """
  def delete_log(%Log{} = log) do
    Repo.delete(log)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking log changes.

  ## Examples

      iex> change_log(log)
      %Ecto.Changeset{source: %Log{}}

  """
  def change_log(%Log{} = log) do
    Log.changeset(log, %{})
  end

